    private void btnAlugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlugarActionPerformed
        try {
            String data_inicial, data_final, status;
            int cod_cliente, cod_livro;
            
            data_inicial = txtData_Inicial.getText();
            data_final = txtData_final.getText();
            status = "Alugado";
            cod_cliente = Integer.parseInt(txtCod_cliente.getText());
            cod_livro = Integer.parseInt(txtCod_Livro.getText());
            
            AlugarDTO objalugardto = new AlugarDTO();
            objalugardto.setCod_cliente(cod_cliente);
            objalugardto.setCod_livro(cod_livro);
            objalugardto.setData_final(data_final);
            objalugardto.setData_inicial(data_inicial);
            objalugardto.setStatus(status);
            
            AlugarDAO objalugardao = new AlugarDAO();
            objalugardao.alugar(objalugardto);
            
            JOptionPane.showMessageDialog(null, "Aluguel realizado com sucesso");
            
            txtAutor_Livro.setText("");
            txtBairro.setText("");
            txtCate_livro.setText("");
            txtCod_Livro.setText("");
            txtCod_cliente.setText("");
            txtCpf.setText("");
            txtData_Inicial.setText("");
            txtData_final.setText("");
            txtIsbn.setText("");
            txtNome_Cliente.setText("");
            txtNome_Livro.setText("");
            txtNum_Cliente1.setText("");
            txtRua_Cliente.setText("");
            txtTelefone.setText("");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAlugarActionPerformed

