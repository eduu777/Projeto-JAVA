    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        int cod_livro;
        cod_livro = Integer.parseInt(txtCod_Livro.getText());
        
        LivroDAO objlivrodao = new LivroDAO();
        LivroDTO objlivrodto = new LivroDTO();
        
        objlivrodto.setCod(cod_livro);
        try {
            objlivrodao.selLivroCod(objlivrodto);
            txtNome_Livro.setText(objlivrodto.getNome());
            txtAutor_Livro.setText(objlivrodto.getAutor());
            txtCate_livro.setText(objlivrodto.getCategoria());
            txtIsbn.setText(Integer.toString(objlivrodto.getIsbn()));
                
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        int cpf;
        cpf = Integer.parseInt(txtCpf.getText());
        
        ClienteDAO objclientedao = new ClienteDAO();
        ClienteDTO objclientedto = new ClienteDTO();
        
        objclientedto.setCpf(cpf);
        try {
            objclientedao.selClienteCpf(objclientedto);
            txtNome_Cliente.setText(objclientedto.getNome());
            txtBairro.setText(objclientedto.getBairro());
            txtRua_Cliente.setText(objclientedto.getRua());
            txtCod_cliente.setText(Integer.toString(objclientedto.getNumero()));
            txtTelefone.setText(objclientedto.getTelefone());
            txtCod_cliente.setText(Integer.toString(objclientedto.getCod()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

