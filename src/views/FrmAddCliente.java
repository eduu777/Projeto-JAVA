    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        try {
            String cliente_nome, cliente_rua, cliente_bairro, cliente_telefone;
            int cliente_cpf, cliente_numero;

            cliente_nome = txtNome.getText();
            cliente_rua = txtRua.getText();
            cliente_bairro = txtBairro.getText();
            cliente_telefone = txtTelefone.getText();
            cliente_cpf = Integer.parseInt(txtCpf.getText());
            cliente_numero = Integer.parseInt(txtNumero.getText());
            
            ClienteDTO objclientedto = new ClienteDTO();
            objclientedto.setNome(cliente_nome);
            objclientedto.setCpf(cliente_cpf);
            objclientedto.setRua(cliente_rua);
            objclientedto.setBairro(cliente_bairro);
            objclientedto.setNumero(cliente_numero);
            objclientedto.setTelefone(cliente_telefone);

            ClienteDAO objclientedao = new ClienteDAO();
            boolean rscliente = objclientedao.addCliente(objclientedto);
            
            JOptionPane.showMessageDialog(null, "Cliente Cadastrado");
            
            txtNome.setText("");
            txtBairro.setText("");
            txtCpf.setText("");
            txtNumero.setText("");
            txtRua.setText("");
            txtTelefone.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"ERRO frmAddCliente" + e);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

