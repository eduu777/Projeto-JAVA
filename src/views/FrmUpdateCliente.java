    private void btnEnviar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviar1ActionPerformed
        int cod, cpf, numero;
        String nome, bairro, rua, telefone;
        
        cod = Integer.parseInt(txtCod.getText());
        cpf = Integer.parseInt(txtCpf.getText());
        numero = Integer.parseInt(txtNumero.getText());
        nome = txtNome.getText();
        bairro = txtBairro.getText();
        rua = txtRua.getText();
        telefone = txtTelefone.getText();
        
        ClienteDTO objclientedto = new ClienteDTO();
        objclientedto.setCod(cod);
        objclientedto.setCpf(cpf);
        objclientedto.setNumero(numero);
        objclientedto.setNome(nome);
        objclientedto.setBairro(bairro);
        objclientedto.setRua(rua);
        objclientedto.setTelefone(telefone);
        
        try {
            ClienteDAO objclientedao = new ClienteDAO();
            objclientedao.updateCliente(objclientedto);
            
            objclientedao.selClienteCod(objclientedto);
            txtNome.setText(objclientedto.getNome());
            txtBairro.setText(objclientedto.getBairro());
            txtCpf.setText(Integer.toString(objclientedto.getCpf()));
            txtRua.setText(objclientedto.getRua());
            txtNumero.setText(Integer.toString(objclientedto.getNumero()));
            txtTelefone.setText(objclientedto.getTelefone());
            txtCod.setText(Integer.toString(objclientedto.getCod()));
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_btnEnviar1ActionPerformed

